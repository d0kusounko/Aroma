#pragma once

#include <atomic>
#include "Typedef.h"

namespace aroma
{

//---------------------------------------------------------------------------
//!	@brief		参照カウンター付きベースオブジェクト.
//---------------------------------------------------------------------------
class RefObject
{
public:
	//-----------------------------------------------------------------------
	//! @brief		コンストラクタ.
	//-----------------------------------------------------------------------
	RefObject()
		: _refCount(1)
	{}
	//-----------------------------------------------------------------------
	//! @brief		デストラクタ.
	//-----------------------------------------------------------------------
	virtual ~RefObject() {}

	//-----------------------------------------------------------------------
	//!	@brief		解放（参照カウンターを1減少).
	//!
	//!	@return		減少後の参照カウント値.
	//! @note		カウンターが0になったら解放処理を行います.
	//-----------------------------------------------------------------------
	s32 Release();

	//-----------------------------------------------------------------------
	//!	@brief		参照カウンタを1増加.
	//!
	//!	@return		増加後の参照カウント値.
	//-----------------------------------------------------------------------
	s32 AddRef();

	//-----------------------------------------------------------------------
	//!	@brief		参照カウントを取得.
	//!
	//!	@return		参照カウント数.
	//-----------------------------------------------------------------------
	s32 GetCount() const;

private:
	volatile std::atomic< s32 >	_refCount;	//!< 参照カウント.
};

} // namespace aroma
